package bitmagic;

public class Demo1 {
    public static void main(String[] args) {
        int x = 3, y = 6;
        System.out.println(x & y);  // AND
        System.out.println(x | y);  // OR
        System.out.println(x ^ y);  //X-OR   //when two input bits are different  produce 1 or else 0
    }
}
